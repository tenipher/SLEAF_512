int encrypt(char *dest, const char *src, size_t size) {
    size_t i = 0, j = 0;
    while(i < size) {
        char val = src[i++];
        while(val != 0) {
            if(val >= 'A') {
                dest[j++] = '@';
                val -= 'A';
            } else if(val >= '0') {
                dest[j++] = '%';
                val -= '0';
            } else if(val > 7) {
                dest[j++] = '?';
                val -= 8;
            } else if(val > 3) {
                dest[j++] = '#';
                val -= 4;
            } else if(val > 0) {
                dest[j++] = '.';
                val -= 1;
            }
            for(size_t k = 0; k < rand() % 32; k++) {
                if(rand() % 2) {
                    dest[j++] = (rand() % 26) + 'A';
                } if(rand() % 2) {
                    dest[j++] = (rand() % 10) + '0';
                } if(rand() % 2) {
                    dest[j++] = (rand() % 26) + 'a';
                }
            }
        }
        dest[j++] = '!';
    }
    dest[j++] = '\0';
    return 0;
}

int decrypt(char *dest, const char *src, size_t size) {
    size_t i = 0, j = 0;

    while(i < size) {
        char val = 0;
        while(src[i] != '!') {
            switch(src[i]) {
            case '@':
                val += 'A';
                break;
            case '%':
                val += '0';
                break;
            case '?':
                val += 8;
                break;
            case '#':
                val += 4;
                break;
            case '.':
                val += 1;
                break;
            default:
                break;
            }
            ++i;
        }
        ++i;

        dest[j++] = val;
    }
    dest[j++] = '\0';
    return 0;
}

